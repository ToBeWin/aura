pub fn normalize_to_simplified_chinese(input: &str) -> String {
    let mut value = input.to_string();

    for (traditional, simplified) in TRADITIONAL_TO_SIMPLIFIED {
        if value.contains(traditional) {
            value = value.replace(traditional, simplified);
        }
    }

    value
}


#[cfg(test)]
mod tests {
    use super::normalize_to_simplified_chinese;

    #[test]
    fn normalizes_common_traditional_characters() {
        let result = normalize_to_simplified_chinese("這個問題為什麼會顯示繁體中文？");
        assert_eq!(result, "这个问题为什么会显示繁体中文？");
    }

    #[test]
    fn normalizes_mixed_traditional_voice_text() {
        let result = normalize_to_simplified_chinese(
            "我們現在應該處理這個問題，為什麼轉寫結果會混合繁體和簡體？",
        );
        assert_eq!(result, "我们现在应该处理这个问题，为什么转写结果会混合繁体和简体？");
    }
}
